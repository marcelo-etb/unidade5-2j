package exemplo4;
public class Aluno {
    public double getMedia(double n1, double n2) {
        return (n1 + n2) / 2;
    }
    
    public double getMedia(int n1, double n2) {
        return (n1 + n2) / 2;
    }
    
    public double getMedia(double n1, double n2, double n3) {
        return (n1 + n2 + n3) / 3;
    }
}
